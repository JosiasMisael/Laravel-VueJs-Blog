    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\User  $user
     * @param  \App\Post  $post
     * @return mixed
     */
    public function view(User $user, Post $post)
    {
        return  $user->id === $post->user_id //Se crea un politica para verificar que el usuario autenticado seal el mismo del post
        || $user->hasPermissionTo('View posts');
    }

